package fb;

class switchoOperator {
	public static void main(String args[])
	{
		String a = "456";
		switch(a)
		{
		case "abc":
			System.out.println("monday");
			break;
		case "def":
			System.out.println('t');
			break;
		case "geh":
			System.out.println('4');
			break;
		case "ijk":
			 System.out.println('d');
			 break;
	    default:
	    	System.out.println("error");
			
	}

}
}